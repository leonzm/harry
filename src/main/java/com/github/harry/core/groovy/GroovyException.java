public class GroovyException extends Exception{

    public GroovyException() {
        super();
    }

    public GroovyException(String message) {
        super(message);
    }

    public GroovyException(String message, Throwable cause) {
        super(message, cause);
    }

    public GroovyException(Throwable cause) {
        super(cause);
    }

}
